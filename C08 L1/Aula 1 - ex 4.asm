li $t1, 1
li $t2, 2
li $t3, 4

and $t0, $t0, 1		# 0xff & 1
or $t2, $t2, 4		# 2 | 4

srl $t3, $t3, 1		# 4 >> 1
mul $t3, $t3, 10	# 10 * (4 >> 1)

sll $t1, $t1, 1		# 1 << 1

div $t1, $t3, $t1	# [10 * (4 >> 1)] / (1 << 1)

sub $t0, $t0, $t2	# (0xff & 1) - (2 | 4)
add $t0, $t0, $t1	# (0xff & 1) - (2 | 4) + [10 * (4 >> 1)] / (1 << 1)


